fixed_t32 FixedMul1632 (int16_t	a, fixed_t	b) {
    fixed_t_union biga;
	longlong_union llu;
	biga.h.intbits = a;
	biga.h.fracbits = 0;
	llu.l = (biga.w * (long long)b);

	biga.h.intbits = llu.h[2];
	biga.h.fracbits = llu.h[1];
	return biga.w;
}

